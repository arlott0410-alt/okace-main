-- ==============================================================================
-- วิธีตั้ง Cron: อัปเดตโปรไฟล์กะ (ย้ายกะ/สลับกะ) ทุกวัน 00:01 น. (Asia/Bangkok)
-- ==============================================================================
-- ระบบจะรัน apply_scheduled_shift_changes_for_date(วันนั้น) เพื่ออัปเดต profiles.default_shift_id
-- ให้ตรงกับรายการย้ายกะ/สลับกะที่มีผลในวันนั้น (โปรไฟล์ถึงเวลาแล้วจะเปลี่ยนเป็นกะใหม่)
-- ==============================================================================

-- -----------------------------------------------------------------------------
-- ขั้นที่ 1: เปิดใช้ Cron ใน Supabase (ทำครั้งเดียว)
-- -----------------------------------------------------------------------------
-- 1. เปิด Supabase Dashboard → โปรเจกต์ของคุณ
-- 2. ไปที่เมนู **Integrations** (แถบซ้าย)
-- 3. หา **Cron** (Cron Postgres Module) → กด **Enable** หรือ **Install**
--    หรือไปที่: Database → Extensions → ค้นหา pg_cron → Enable
-- 4. ถ้ามีให้เปิดใช้ extension ด้วย SQL ด้านล่าง (รันใน SQL Editor):

CREATE EXTENSION IF NOT EXISTS pg_cron;

-- -----------------------------------------------------------------------------
-- ขั้นที่ 2: ลงทะเบียน Job (รันใน SQL Editor ครั้งเดียว)
-- -----------------------------------------------------------------------------
-- เวลา 00:01 น. Bangkok = 17:01 น. UTC (วันเดียวกัน)
-- Cron expression: นาที ชม วัน เดือน วันในสัปดาห์ → '1 17 * * *'

SELECT cron.schedule(
  'apply_shift_changes_daily',           -- ชื่อ job (ห้ามซ้ำ, case-sensitive)
  '1 17 * * *',                         -- ทุกวัน 17:01 UTC = 00:01 Bangkok
  $$SELECT run_apply_shift_changes_and_log()$$
);

-- ถ้าต้องการลบ job เดิมก่อน (ถ้ามีแล้วสร้างใหม่):
-- SELECT cron.unschedule('apply_shift_changes_daily');

-- -----------------------------------------------------------------------------
-- ขั้นที่ 3: ตรวจสอบว่า Job ถูกสร้างแล้ว
-- -----------------------------------------------------------------------------
-- รันคำสั่งนี้แล้วควรเห็นแถวชื่อ apply_shift_changes_daily:

SELECT jobid, jobname, schedule, command FROM cron.job;

-- -----------------------------------------------------------------------------
-- ทดสอบรันด้วยมือ (ไม่ต้องรอ 00:01)
-- -----------------------------------------------------------------------------
-- รัน apply สำหรับวันนี้ (ตาม timezone Bangkok) พร้อมบันทึก log:

SELECT run_apply_shift_changes_and_log();

-- ดู log ล่าสุด (ว่า success หรือ error):

SELECT * FROM cron_runs ORDER BY ran_at DESC LIMIT 10;

